package nl.utwente.ing.model;

public class HistoryItem {
    private long open;
    private long close;
    private long high;
    private long low;
    private long volume;

    public HistoryItem(long start) {
        this.open = start;
        this.close = start;
        this.high = start;
        this.low = start;
    }

    public long getOpen() {
        return open;
    }

    public void setOpen(long open) {
        this.open = open;
    }

    public long getClose() {
        return close;
    }

    public long getHigh() {
        return high;
    }

    public void setHigh(long high) {
        this.high = high;
    }

    public long getLow() {
        return low;
    }

    public void setLow(long low) {
        this.low = low;
    }

    public long getVolume() {
        return volume;
    }

    public void setVolume(long volume) {
        this.volume = volume;
    }
}
